namespace DevAll\Abandoned\Controller\Adminhtml\History;

use Magento\Backend\App\Action;
use Magento\Backend\App\Action\Context;
use Magento\Framework\App\Action\HttpGetActionInterface;
use Magento\Framework\View\Result\Page;
use Magento\Framework\View\Result\PageFactory;

/**
 * Class Index for History page
 */
class Index extends Action implements HttpGetActionInterface
{
    const ADMIN_RESOURCE = 'DevAll_Abandoned::abandoned';

    /** @var PageFactory */
    protected $pageFactory;

    /**
     * @param Context $context
     * @param PageFactory $pageFactory
     */
    public function __construct(
        Context $context,
        PageFactory $pageFactory
    ) {
        parent::__construct($context);
        $this->pageFactory = $pageFactory;
    }

    /**
     * @return Page
     */
    public function execute(): Page
    {
        $page = $this->pageFactory->create();
        $page->setActiveMenu('DevAll_Abandoned::abandoned');
        $page->getConfig()->getTitle()->prepend(__('History'));

        return $page;
    }
}
